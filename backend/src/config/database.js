const mongoose = require('mongoose');

const connectDB = async () => {
  try {
    const mongoURI = process.env.NODE_ENV === 'production' 
      ? process.env.MONGODB_URI_PROD 
      : process.env.MONGODB_URI || 'mongodb://localhost:27017/aspiro';

    const conn = await mongoose.connect(mongoURI);

    console.log(`ğŸ“Š MongoDB Connected: ${conn.connection.host}`);
    
    // Handle connection events
    mongoose.connection.on('error', (err) => {
      console.error('âŒ MongoDB connection error:', err);
    });

    mongoose.connection.on('disconnected', () => {
      console.log('ğŸ“Š MongoDB disconnected');
    });

    // Graceful shutdown
    process.on('SIGINT', async () => {
      await mongoose.connection.close();
      console.log('ğŸ“Š MongoDB connection closed through app termination');
      process.exit(0);
    });

  } catch (error) {
    console.error('âŒ Database connection failed:', error.message);
    console.log('ğŸ’¡ Make sure MongoDB is running locally or update MONGODB_URI in .env');
    process.exit(1);
  }
};

module.exports = connectDB;
